<template>
  <div class="corpo">
    <h1 class="centralizado">{{ titulo }}</h1>
    <h2 class="centralizado" v-text="subtitulo"></h2>

    <input type="search" class="filtro" v-on:input="filtro = $event.target.value" placeholder="Filtre pelo tÃ­tulo">

    <ul class="lista-fotos">
      <li class="lista-fotos-item" v-for="foto in fotosComFiltro">

        <meu-painel :titulo="foto.titulo">
          <imagem-responsiva :url="foto.url" :titulo="foto.titulo"/>
          <meu-botao rotulo="Remover" tipo="button" @click.native="remove(foto)"/>
        </meu-painel>

      </li>
    </ul>
  </div>
</template>

<script>
import Painel from '../shared/painel/Painel.vue';
import ImagemResponsiva from '../shared/imagem-responsiva/ImagemResponsiva.vue';
import Botao from '../shared/botao/Botao.vue';

export default {

    components: {
      'meu-painel': Painel,
      'imagem-responsiva': ImagemResponsiva,
      'meu-botao': Botao
    },

    data() {
        return {
            titulo: 'Alurapic',
            subtitulo: 'Lorem ipsum dolor sit amet, consectetur adipisicing elit.',
            fotos: [],
            filtro: ''
        }
    },

    computed: {
        fotosComFiltro() {
            if(this.filtro) {
                let exp = new RegExp(this.filtro.trim(), 'i');
                return this.fotos.filter(item => exp.test(item.titulo));
            }

            return this.fotos;
        }
    },

    created() {
        this.$http.get('http://localhost:3000/v1/fotos')
            .then(res => res.json())
            .then(fotos => this.fotos = fotos, err => console.log(err));
    },

    methods: {
        remove(foto) {
            if(confirm('Confirma?')) {
                alert('Remover a foto ' + foto.titulo);
            }
        }
    }
}
</script>

